export default function LegalHighlights({ highlights }) {
  const sectionRef = useRef(null);

  useEffect(() => {
    const ctx = gsap.context(() => {
      gsap.utils.toArray(".highlight-card").forEach((card, index) => {
        gsap.fromTo(
          card,
          { opacity: 0, y: 30 },
          {
            opacity: 1,
            y: 0,
            duration: 0.7,
            ease: "power3.out",
            delay: index * 0.1,
            scrollTrigger: {
              trigger: sectionRef.current,
              start: "top 80%",
              toggleActions: "play none none none",
            },
          }
        );
      });
    }, sectionRef);

    return () => ctx.revert();
  }, []);

  return (
    <section
      ref={sectionRef}
      className="py-16 lg:py-20 px-6 md:px-10 border-t border-[#222] 3xl:max-w-[1400px] 3xl:mx-auto"
    >
      <div className="text-xs uppercase tracking-[0.28em] text-textGray mb-8">
        Key Points
      </div>
      <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-4">
        {highlights.map((item) => (
          <div
            key={item.label}
            className="highlight-card group rounded-2xl border border-[#222] bg-[#0a0a0a] p-5 lg:p-6 hover:border-[#333] transition-colors"
          >
            <div className="w-12 h-12 rounded-xl bg-[#111] border border-[#222] flex items-center justify-center text-textGray group-hover:text-white group-hover:border-[#333] transition-colors mb-4">
              {iconMap[item.icon]}
            </div>
            <h3 className="text-lg font-semibold tracking-tight mb-1">
              {item.label}
            </h3>
            <p className="text-sm text-textGray leading-relaxed">
              {item.desc}
            </p>
          </div>
        ))}
      </div>
    </section>
  );
}
